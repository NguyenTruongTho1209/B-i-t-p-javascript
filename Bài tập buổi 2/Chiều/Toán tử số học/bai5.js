var r = 2;

// a
var duongKinh = 2*r;
console.log('Đường kính của hình bán nguyệt là : ' + duongKinh);

// b
var chuVi = 3.14*r;
console.log('Chu vi của hình bán nguyệt là : '  + chuVi);

// c
var dienTich = 3.14*r**2 / 2;
console.log('Diện tích của hình bán nguyệt là : ' + dienTich);
